"""Generate KSeF FA(3) XML fixtures for categorization eval from manifest.json.

Reads ``tests/fixtures/labeled/manifest.json`` and writes one XML per entry
to ``tests/fixtures/labeled/{id}.xml``. Generated XMLs are valid FA(3) per
the namespace ``http://crd.gov.pl/wzor/2025/06/25/13775/`` and parseable by
``app.services.ksef_parser.parse_ksef``.

Validates upfront that every ``expected`` literal in the manifest is a
member of ``app.schemas.category.InvoiceCategory``. Exits 1 with a list of
offending entries if not — so a typo in the manifest can never make a
correct LLM prediction look wrong in the eval report.

Usage::

    python scripts/generate_eval_fixtures.py
    python scripts/generate_eval_fixtures.py --check   # validate only, no write
"""

from __future__ import annotations

import argparse
import json
import sys
from decimal import ROUND_HALF_UP, Decimal
from pathlib import Path
from xml.sax.saxutils import escape

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

# Force UTF-8 stdout/stderr — Windows console defaults to cp1250 which
# chokes on the polish category strings and the ∈ symbol in output.
if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8", errors="replace")
    sys.stderr.reconfigure(encoding="utf-8", errors="replace")

from app.schemas.category import InvoiceCategory  # noqa: E402

MANIFEST_PATH = ROOT / "tests" / "fixtures" / "labeled" / "manifest.json"
OUT_DIR = ROOT / "tests" / "fixtures" / "labeled"
KSEF_NS = "http://crd.gov.pl/wzor/2025/06/25/13775/"
ISSUE_DATE = "2026-11-15"
VAT_RATE = Decimal("0.23")
BUYER_NAME = "Test Buyer Sp. z o.o."
BUYER_NIP = "0000000000"

VALID_CATEGORIES = {c.value for c in InvoiceCategory}
REQUIRED_FIELDS = {"id", "expected", "seller", "seller_nip", "lines", "intent"}
REQUIRED_LINE_FIELDS = {"desc", "qty", "net"}


def _q(value: Decimal) -> str:
    return str(value.quantize(Decimal("0.01"), rounding=ROUND_HALF_UP))


def validate(entries: list[dict]) -> list[str]:
    """Return list of human-readable errors; empty list = manifest is valid."""
    errors: list[str] = []
    seen_ids: set[str] = set()
    for i, entry in enumerate(entries):
        missing = REQUIRED_FIELDS - entry.keys()
        if missing:
            errors.append(f"[entry #{i}] missing fields: {sorted(missing)}")
            continue
        eid = entry["id"]
        if eid in seen_ids:
            errors.append(f"[{eid}] duplicate id")
        seen_ids.add(eid)
        if entry["expected"] not in VALID_CATEGORIES:
            errors.append(
                f"[{eid}] expected={entry['expected']!r} is NOT in InvoiceCategory enum.\n"
                f"           Valid values: {sorted(VALID_CATEGORIES)}"
            )
        nip = entry["seller_nip"]
        if not (isinstance(nip, str) and len(nip) == 10 and nip.isdigit()):
            errors.append(f"[{eid}] seller_nip must be 10 digits, got {nip!r}")
        if not entry["lines"]:
            errors.append(f"[{eid}] lines must be non-empty")
        for j, line in enumerate(entry["lines"]):
            line_missing = REQUIRED_LINE_FIELDS - set(line.keys())
            if line_missing:
                errors.append(f"[{eid}] line[{j}] missing fields: {sorted(line_missing)}")
                continue
            try:
                Decimal(str(line["qty"]))
                Decimal(line["net"])
            except Exception as exc:
                errors.append(f"[{eid}] line[{j}] invalid Decimal value: {exc}")
    return errors


def build_xml(entry: dict) -> str:
    """Render one manifest entry as FA(3) XML.

    Totals are computed from line items (net = sum line totals, vat = net *
    0.23, gross = net + vat) so the manifest stays minimal — you only pick
    line descriptions and unit prices, totals follow.
    """
    seller_name = escape(entry["seller"])
    seller_nip = entry["seller_nip"]
    invoice_number = f"FV/EVAL/{entry['id']}/2026"

    line_blocks: list[str] = []
    net_total = Decimal("0.00")
    for idx, line in enumerate(entry["lines"], start=1):
        qty = Decimal(str(line["qty"]))
        unit_net = Decimal(line["net"])
        line_total = (qty * unit_net).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)
        net_total += line_total
        line_blocks.append(
            f"""    <FaWiersz>
      <NrWierszaFa>{idx}</NrWierszaFa>
      <P_7>{escape(line["desc"])}</P_7>
      <P_8A>szt</P_8A>
      <P_8B>{qty}</P_8B>
      <P_9A>{_q(unit_net)}</P_9A>
      <P_11>{_q(line_total)}</P_11>
      <P_12>23</P_12>
    </FaWiersz>"""
        )

    vat_total = (net_total * VAT_RATE).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP)
    gross_total = net_total + vat_total

    return f"""<?xml version="1.0" encoding="UTF-8"?>
<!--
  Eval fixture (id={entry["id"]}).
  Auto-generated by scripts/generate_eval_fixtures.py — DO NOT EDIT BY HAND.
  Re-run the generator to regenerate after manifest changes.

  Expected category: {entry["expected"]}
  Intent: {entry["intent"]}
-->
<Faktura xmlns="{KSEF_NS}">
  <Naglowek>
    <KodFormularza kodSystemowy="FA (3)" wersjaSchemy="1-0E">FA</KodFormularza>
    <WariantFormularza>3</WariantFormularza>
    <DataWytworzeniaFa>{ISSUE_DATE}T10:00:00Z</DataWytworzeniaFa>
    <SystemInfo>InvoiceProcessor Eval Fixture</SystemInfo>
  </Naglowek>
  <Podmiot1>
    <DaneIdentyfikacyjne>
      <NIP>{seller_nip}</NIP>
      <Nazwa>{seller_name}</Nazwa>
    </DaneIdentyfikacyjne>
    <Adres>
      <KodKraju>PL</KodKraju>
      <AdresL1>ul. Testowa 1</AdresL1>
      <AdresL2>00-001 Warszawa</AdresL2>
    </Adres>
  </Podmiot1>
  <Podmiot2>
    <DaneIdentyfikacyjne>
      <NIP>{BUYER_NIP}</NIP>
      <Nazwa>{BUYER_NAME}</Nazwa>
    </DaneIdentyfikacyjne>
    <Adres>
      <KodKraju>PL</KodKraju>
      <AdresL1>al. Buyer 99</AdresL1>
      <AdresL2>00-002 Warszawa</AdresL2>
    </Adres>
  </Podmiot2>
  <Fa>
    <KodWaluty>PLN</KodWaluty>
    <P_1>{ISSUE_DATE}</P_1>
    <P_2>{invoice_number}</P_2>
    <P_13_1>{_q(net_total)}</P_13_1>
    <P_14_1>{_q(vat_total)}</P_14_1>
    <P_15>{_q(gross_total)}</P_15>
{chr(10).join(line_blocks)}
  </Fa>
</Faktura>
"""


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument(
        "--check",
        action="store_true",
        help="Validate manifest only, do not write XML files.",
    )
    args = parser.parse_args()

    if not MANIFEST_PATH.exists():
        print(f"ERROR: manifest not found at {MANIFEST_PATH}", file=sys.stderr)
        return 1

    entries = json.loads(MANIFEST_PATH.read_text(encoding="utf-8"))

    errors = validate(entries)
    if errors:
        print("Manifest validation failed:", file=sys.stderr)
        for err in errors:
            print(f"  - {err}", file=sys.stderr)
        return 1

    if args.check:
        print(f"OK: {len(entries)} entries valid (categories ∈ enum, ids unique, NIPs 10 digits).")
        return 0

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    for entry in entries:
        xml = build_xml(entry)
        path = OUT_DIR / f"{entry['id']}.xml"
        path.write_text(xml, encoding="utf-8")
        print(f"  wrote {path.relative_to(ROOT)}")

    print(f"\nGenerated {len(entries)} fixtures in {OUT_DIR.relative_to(ROOT)}/")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
